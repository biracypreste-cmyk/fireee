// Function to get RSS feed URL for a team
export function getTeamRssFeed(teamName: string): string | null {
  // Try exact match first
  if (teamRssFeeds[teamName]) {
    return teamRssFeeds[teamName];
  }
  
  // Try partial match
  for (const [key, feed] of Object.entries(teamRssFeeds)) {
    if (teamName.includes(key) || key.includes(teamName)) {
      return feed;
    }
  }
  
  // No specific feed found
  return null;
}

// Function to get all team names with RSS feeds
export function getTeamsWithRssFeeds(): string[] {
  return Object.keys(teamRssFeeds).filter((key, index, arr) => {
    // Remove duplicates (keep first occurrence)
    return arr.indexOf(key) === index;
  });
}
