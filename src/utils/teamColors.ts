// Function to get team colors
export function getTeamColors(teamName: string): TeamColors {
  // Try exact match first
  if (brazilianTeamColors[teamName]) {
    return brazilianTeamColors[teamName];
  }
  
  // Try partial match
  for (const [key, colors] of Object.entries(brazilianTeamColors)) {
    if (teamName.includes(key) || key.includes(teamName)) {
      return colors;
    }
  }
  
  // Default colors (RedFlix)
  return {
    primary: '#E50914',
    secondary: '#000000',
    accent: '#FFD700',
    gradient: 'from-[#E50914] via-[#8B0000] to-[#000000]'
  };
}

// Function to get text color based on background
export function getTextColor(bgColor: string): string {
  // Simple luminance check
  const hex = bgColor.replace('#', '');
  const r = parseInt(hex.substr(0, 2), 16);
  const g = parseInt(hex.substr(2, 2), 16);
  const b = parseInt(hex.substr(4, 2), 16);
  const luminance = (0.299 * r + 0.587 * g + 0.114 * b) / 255;
  
  return luminance > 0.5 ? '#000000' : '#FFFFFF';
}
